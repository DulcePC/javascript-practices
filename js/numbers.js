//Operaciones en javascript
let resultado;
resultado = numero1 + numero2;
console.log(resultado);

resultado = numero2 * numero4;
console.log(resultado);

resultado = numero2 / numero2;
console.log(resultado);
//modulo
resultado = numero3 % 30;
console.log(resultado);

resultado = Math.floor(numero3);
console.log(resultado);

